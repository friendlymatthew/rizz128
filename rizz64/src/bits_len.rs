pub(crate) fn len_u64(x: u64) -> usize {
    let mut x = x;
    let mut n = 0;

    if x >= 1 << 32 {
        x >>= 32;
        n = 32;
    }

    if x >= 1 << 16 {
        x >>= 16;
        n += 16;
    }

    if x >= 1 << 8 {
        x >>= 8;
        n += 8;
    }

    return n + LEN8TAB[x as usize] as usize;
}
